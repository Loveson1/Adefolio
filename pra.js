
let a = 10;
let b = 5;
let c = 2;
let isAdmin = true;
let names = "John";

// 1. Addition
console.log(a + b); 

// 2. Subtraction
console.log(a - b); 

// 3. Multiplication
console.log(a * b);

// 4. Division
console.log(a / b); 

// 5. Modulus
console.log(a % b); 

// 6. Exponentiation
console.log(a ** c); 

// 7. Assignment
let x = 5;
x += a;
console.log(x); 

// 8. Comparison
console.log(a > b);

// 9. Equality
console.log(a === 10); 

// 10. Inequality
console.log(a !== b); 

// 11. Logical AND
console.log(isAdmin && true); 

// 12. Logical OR
console.log(isAdmin || false);

// 13. Logical NOT
console.log(!isAdmin); 

// 14. String concatenation
console.log(names + " is admin: " + isAdmin); 

// 15. Ternary operator
console.log(a > b ? "a is greater" : "b is greater");

// 16. Greater than or equal
console.log(a >= b); 

// 17. Less than or equal
console.log(b <= a); 

// 18. Increment
